class Tag:
    def __init__(self, name: str, subsystem: str, independent_variable: bool, measurement_type: str, description: str,
                 data_type: str) -> None:
        self.name = name
        self.subsystem = subsystem
        self.measurement_type = measurement_type
        self.description = description
        self.independent_variable = independent_variable


class PackageModel:
    def __init__(self, model_number, indexes, ignored, tags):
        self.model_number = model_number
        self.indexes = indexes
        self.ignored = ignored
        self.tags = tags

    def independent_variables(self):
        return [x for x in self.tags if x.independent_variable]

    def subsystem(self, system_name):
        return [x for x in self.tags if x.subsystem == system_name]


